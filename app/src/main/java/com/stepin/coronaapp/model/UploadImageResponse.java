public class UploadImageResponse {
    public String status;
    public String msg;
    public String imageName;

    public UploadImageResponse() {
    }

    public String getStatus() {
        return status;
    }

    public String getMsg() {
        return msg;
    }

    public String getImageName() {
        return imageName;
    }
}
